class Solution
{
    public:
    //Function to find the minimum number of coins to make the change 
    //for value using the coins of given denominations.
    long long minimumNumberOfCoins(int coins[],int numberOfCoins,int v)
    {
        vector<long long> dp(v+1,INT_MAX);
        dp[0]=0;
        //cout<<dp[v]<<"v\n";
        
        for(int i=0;i<numberOfCoins;i++)
        {
           int c = coins[i];
            for(int j=1;j<=v;j++)
            {
                if(j-c>=0)
                    dp[j]=min(dp[j],dp[j-c]+1);
            }
        }
        if(dp[v]==INT_MAX)return -1;
        else return dp[v];
    }
};

